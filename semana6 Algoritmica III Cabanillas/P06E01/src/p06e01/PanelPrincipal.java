    private void btnOpcionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpcionesActionPerformed
     txtSalida.setText("");
      if(leeBusca()==1){
       txtSalida.setText("Codigo\tDescripcion\tPrecio\n");
      for(int i=0;i<ap.getN();i++){
          Producto p=ap.getProducto(i);
          imprime(p.getCodigo()+"\t"+p.getDescripcion()+"\t"+ap.aumentarValor(p, leeMenor(), leeMayor(), leeCuanto()));
      }
      }  
      
      if(leeBusca()==2){
       txtSalida.setText("Codigo\tDescripcion\tPrecio\n");
      for(int i=0;i<ap.getN();i++){
          Producto p=ap.getProducto(i);
          imprime(p.getCodigo()+"\t"+p.getDescripcion()+"\t"+ap.disminuirValor(p,leeMenor() , leeMayor(), leeCuanto()));
      }
      } 
        // TODO add your handling code here:
    }//GEN-LAST:event_btnOpcionesActionPerformed

