    private int leeBusca(){return cboOpcion.getSelectedIndex();}
    private int leeMayor(){return Integer.parseInt(txtMayorA.getText());}
    
    private int leeMenor(){return Integer.parseInt(txtMenorA.getText());}
    
    private int leeCuanto(){return Integer.parseInt(txtCuanto.getText());}
    
    private String  leeBusque(){return txtBuscar.getText();}
    
    private String leeCodigo(){
        return txtCodigo.getText(); 
    }
    private String leeDescripcion(){
        return txtDescripcion.getText();
    }
    private double leePrecio(){
        return Double.parseDouble(txtPrecio.getText());
    }
    
    public void lista(Producto p){
        imprime("Codigo\t:"+p.getCodigo());
        imprime("Descripcion\t:"+p.getDescripcion());
        imprime("Precio\t:"+p.getPrecio());
    }
    
    public void imprime(String s){
        txtSalida.append(s+"\n");
    }
public void listado(){
        txtSalida.setText("Codigo\tDescripcion\tPrecio\n");
      for(int i=0;i<ap.getN();i++){
          Producto p=ap.getProducto(i);
          imprime(p.getCodigo()+"\t"+p.getDescripcion()+"\t"+p.getPrecio());
      }
    }

